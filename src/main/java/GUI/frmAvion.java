    private void jtAvionesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAvionesMousePressed
        // TODO add your handling code here:
        _indiceSeleccionado= this.jtAviones.getSelectedRow();
        if(_indiceSeleccionado!=-1){
            Avion infoAv=MDIPrincipal.gAvion.getAvionByIndex(_indiceSeleccionado);
            mostrarElemento(infoAv);
        }
    }//GEN-LAST:event_jtAvionesMousePressed
    /**
     * MUENTRA LA INFO DEL ELEMENTO SELECCIONADO
     * @param avion OBJETO AVION
     */
    public void mostrarElemento(Avion avion){
        this.txtCodigo.setText(avion.get_codigoAvion());
        this.txtArolinea.setText(avion.get_aerolinea());
        this.txtNumAsientos.setText(Integer.toString(avion.get_numeroAsientos()));
    }

