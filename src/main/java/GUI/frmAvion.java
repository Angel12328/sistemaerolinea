    private void btnEliminarAvion1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarAvion1ActionPerformed
        // TODO add your handling code here:
     
        if(_indiceSeleccionado>=0){
            int resultado = JOptionPane.showConfirmDialog(null, "¿Está seguro?", "Warning", JOptionPane.YES_NO_OPTION);
            if(resultado==JOptionPane.YES_NO_OPTION){
               MDIPrincipal.gAvion.eliminarPorIndice(_indiceSeleccionado);
               actualizarElementosTabla();
               AdminSerializacion.serializacion(MDIPrincipal.gAvion, "gAvion.obj");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Favor seleccione el elemento de la tabla que desea eliminar"); 
        }
        
    }//GEN-LAST:event_btnEliminarAvion1ActionPerformed

