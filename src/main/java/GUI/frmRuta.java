    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        //pASO 1. Solicitar la informacion
        
        String codigo, fechaSalida, fechaLlegada,horaSalida,horaLlegada;
        horaSalida=this.txtHoraSalida.getText();
        horaLlegada=this.txtHoraLlegada1.getText();
        codigo=this.txtCodigo.getText();
        fechaSalida=this.txtFechaSalida.getText()+" "+horaSalida;
        fechaLlegada=this.txtFechaLlegada.getText()+" "+horaLlegada;
        
        Aeropuerto _aeropuerto=MDIPrincipal.gAeropuerto.getAeroByIndex(this.cboOrigen.getSelectedIndex());
        Aeropuerto _aeropuerto2=MDIPrincipal.gAeropuerto.getAeroByIndex(this.cboDestino.getSelectedIndex());
        
        //instancia de Ruta
        Ruta _ruta=new Ruta();
        
        try{
        
            //paso3.Setteando los valores 
        _ruta.set_codigo(codigo);
        _ruta.set_origen(_aeropuerto);
        _ruta.set_destino(_aeropuerto2);
        _ruta.setFechSalidaString(this.txtFechaSalida.getText());
        _ruta.setFechLlegadaString(this.txtFechaLlegada.getText());
        _ruta.set_fechasalida(AdminFechas.stringToDate(fechaSalida));
        _ruta.set_fechallegada(AdminFechas.stringToDate(fechaLlegada));
        _ruta.setHoraSalidaString(horaSalida);
        _ruta.setHoraLegadaString(horaLlegada);
        
        //paso 4. 
        if(_agregando){
        MDIPrincipal.gRuta.agregarRuta(_ruta);
        }else{
        
            MDIPrincipal.gRuta.modificarRuta(_indiceSeleccion, _ruta);
        }
      
        
        //Paso 5. Actualizar elementos en la tabla
        actualizarElementosTabla();
        
         //PASO 6: Serializar la informacion
         AdminSerializacion.serializacion(MDIPrincipal.gRuta, "gRuta.obj");

        estadoBotones(true);
        estadoControles(false);
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error" +e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

