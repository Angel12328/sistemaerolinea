            public void actualizarElementosTabla(){
                
                String [] titulos ={"Código","Origen","Destino","Fecha Salida","Fecha Llegada"};
                
                DefaultTableModel dt = new DefaultTableModel(MDIPrincipal.gRuta.getArrayGestion(),titulos);
                
                this.jtRutas.setModel(dt);
                
                
                
            }
            
            
            public void mostrarElemento(Ruta _ruta){
                this.txtCodigo.setText(_ruta.get_codigo());
                this.txtFechaSalida.setText(_ruta.getFechSalidaString());
                this.txtHoraSalida.setText(_ruta.getHoraSalidaString());
                this.txtFechaLlegada.setText(_ruta.getFechLlegadaString());
                this.txtHoraLlegada1.setText(_ruta.getHoraLegadaString());
                
                Aeropuerto _aeropuerto= _ruta.get_origen();
                
                int _indice=MDIPrincipal.gAeropuerto.buscarAeropuerto(_aeropuerto.get_codigo());
                this.cboOrigen.setSelectedIndex(_indice);
                
                Aeropuerto _areopuerto2= _ruta.get_destino();
                
                int _indice2=MDIPrincipal.gAeropuerto.buscarAeropuerto(_areopuerto2.get_codigo());
                this.cboDestino.setSelectedIndex(_indice2);
                
                
                
            }
            
